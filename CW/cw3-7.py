# Змінити попередню програму так, щоб в кінці кожної букви елементів 
# при виводі додавався певний символ, наприклад “#”. 
# (Підказка: цикл for може бути вкладений в інший цикл, 
# а також  треба використати функцію print(“ ”, end=”%”)).

list_4_elements = ['hi', 'hello', 'good', 'morning']

for i in list_4_elements:
    for j in i:
        print(j, end = '#')
print()
    