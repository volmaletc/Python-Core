#Створити список, який містить елементи цілочисельного типу, 
# потім за допомогою циклу перебору змінити тип даних 
# елементів на числа з плаваючою крапкою. 
#(Підказка: використати вбудовану функцію float ()).

list_number = [8,9,5,8,10,9]
print(list_number)
i = 0
for a in list_number:
    list_number[i] = float(a)
    i = i + 1
print(list_number)
