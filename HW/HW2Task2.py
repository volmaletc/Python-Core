a = input ("Введіть чотирицифрове натуральне число: ")
print("Ви ввели число " , a)
b = a
b = list(b)

z = 1
for x in b:
    z *= int(x)
print("Добуток всіх чисел дорівеює ", z)


c = a[::-1]
print("Реверсний порядок чисел дорівнює ", c)

d = sorted(a)
print("Числа відсортовані в порядку зростання", d)
